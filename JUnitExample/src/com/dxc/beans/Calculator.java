package com.dxc.beans;

public class Calculator {

	
	public int add(int n1,int n2) {
		int res;
		res = n1+n2;
		return res;
	}
	
	public int sub(int n1,int n2) {
		int res;
		res = n1-n2;
		return res;
	}
	
	public double div(int n1,int n2) {
		double res;
		res = n1/n2;
		return res;
	}
	
	public int mul(int n1,int n2) {
		int res;
		res = n1*n2;
		return res;
	}
}